public class CoordenatesXYClass {
    float X;
    float Y;

    public CoordenatesXYClass() {
    }

    public CoordenatesXYClass(float x, float y) {
        X = x;
        Y = y;
    }

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }
}
